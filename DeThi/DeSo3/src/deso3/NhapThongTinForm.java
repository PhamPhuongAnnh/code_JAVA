    private void btnThemSinhVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemSinhVienActionPerformed
// nút thêm sinh viên 
        SinhVien sv = new SinhVien();
        sv.setHoTen(txtHoTen.getText());
        sv.setMaSV(txtMaSV.getText());
        sv.setDiemHocPhan(Double.parseDouble(txtDiemHocPhan.getText()));
        sv.setDiemQuaTrinh(Double.parseDouble(txtDiemQuaTrinh.getText()));
        danhSachSinhVien.add(sv);
    }//GEN-LAST:event_btnThemSinhVienActionPerformed
// hiển thị

    public void hienThi() {
//        tableModel.setRowCount(0);
        for (SinhVien item : danhSachSinhVien) {
            int stt = id++;
            tableModel.addRow(new Object[]{stt, item.getMaSV(), item.getHoTen(), txtTenLop.getText(), item.getDiemHocPhan(), item.getDiemQuaTrinh(), item.diemTrungBinh()});
        }
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // thêm lớp học
        hienThi();
    }//GEN-LAST:event_jButton1ActionPerformed

